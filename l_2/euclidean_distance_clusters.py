"""
Вычисление евклидова расстояния между центрами кластеров.

Задача: вычислить евклидово расстояние между кластером c1 и c3 и сравнить с расстоянием между c1 и c2.
"""

# ============== ИМПОРТЫ ==============
# math — встроенная библиотека Python с математическими функциями.
# Нам нужна функция sqrt (квадратный корень) для формулы евклидова расстояния.
import math


# ============== ДАННЫЕ: ЦЕНТРЫ КЛАСТЕРОВ ==============
# Кластер — это группа близких точек. У каждого кластера есть "центр" (одна точка).
# Координаты заданы как [x, y] на плоскости.
# Точка с координатами (1.0, 1.5) — это x=1.0, y=1.5.

c1 = [1.0, 1.5]    # центр "красного" кластера
c2 = [-1.0, -0.5]  # центр "оранжевого" кластера
c3 = [1.0, -0.5]   # центр "зелёного" кластера


# ============== ФУНКЦИЯ ЕВКЛИДОВА РАССТОЯНИЯ ==============
# Евклидово расстояние — это обычное "прямое" расстояние между двумя точками
# (как линейка между ними). Формула для двух точек (x1,y1) и (x2,y2):
#
#   расстояние = sqrt( (x2 - x1)^2 + (y2 - y1)^2 )
#
# p1, p2 — списки из двух чисел [x, y].
# p1[0] — первая координата (x), p1[1] — вторая (y).
def euclidean_distance(p1, p2):
    dx = p2[0] - p1[0]  # разница по оси X
    dy = p2[1] - p1[1]  # разница по оси Y
    # Квадраты нужны, чтобы любое направление давало положительный вклад, потом извлекаем корень.
    return math.sqrt(dx ** 2 + dy ** 2)


# ============== ВЫЧИСЛЕНИЕ РАССТОЯНИЙ ==============
# Расстояние между центром c1 и центром c3.
d_c1_c3 = euclidean_distance(c1, c3)

# Расстояние между центром c1 и центром c2.
d_c1_c2 = euclidean_distance(c1, c2)


# ============== ВЫВОД РЕЗУЛЬТАТОВ ==============
print("Расстояние между кластером c1 и кластером c3:", round(d_c1_c3, 4))
print("Расстояние между кластером c1 и кластером c2:", round(d_c1_c2, 4))
print()

# Сравниваем: какое расстояние меньше?
if d_c1_c3 < d_c1_c2:
    print("Ответ: расстояние между c1 и c3 МЕНЬШЕ, чем между c1 и c2.")
else:
    print("Ответ: расстояние между c1 и c3 БОЛЬШЕ или равно расстоянию между c1 и c2.")

# Кратко: c1 и c3 лежат по вертикали на одной x=1 (разница только по y: 1.5 и -0.5 → 2.0).
# c1 и c2 далеко разнесены и по x, и по y, поэтому расстояние между ними больше (sqrt(8) ≈ 2.83).
